console.log(Math);
console.log(Math.PI);
console.log(Math.abs(-7.8)); // nos devuelve el valor absoluto, la cantidad como tal sin considerar si es negativo o positivom, 7.8
console.log(Math.ceil(7.2)); // siempre redondea hacia arriba, 8
console.log(Math.floor(7.8)); // siempre redondea hacia abajo, 7
console.log(Math.round(7.49)); // siempre redondea hacia arriba o abajo, al mas cercano inmediato
console.log(Math.sqrt(81)); // la raiz cuadrada, seria 9
console.log(Math.pow(2, 5)); // elevear potencias, primer numero base y segundo exponente, 32
console.log(Math.sign(-0.3)); // -1 0 1, indica si el numero es negativo, positivo o 0
console.log(Math.random()); // numero aleatorio entre 0 y 1, para que sea entre 1 y 10, multiplico por 10 y sumo 1
console.log(Math.round(Math.random() * 1000));